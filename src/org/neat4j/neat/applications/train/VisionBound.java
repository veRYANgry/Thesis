package org.neat4j.neat.applications.train;

public class VisionBound {
	public int XVisionStart, XVisionEnd;
	public int YVisionStart, YVisionEnd;
	public VisionBound(int xVisionStart, int xVisionEnd, int yVisionStart,
			int yVisionEnd) {
		super();
		XVisionStart = xVisionStart;
		XVisionEnd = xVisionEnd;
		YVisionStart = yVisionStart;
		YVisionEnd = yVisionEnd;
	}
	
}
